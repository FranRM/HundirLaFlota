    public void insertar(Tableiro t){
        puntu.conectar();
        sumpunt=t.getAcertoH()*10;
        
    try {
            PreparedStatement st = puntu.connect.prepareStatement("insert into xogador (nome, puntos) values (?,?)");
            
            
            st.setString(1, jtID.getText());
            st.setInt(2,sumpunt);

            st.execute();
            
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
}
